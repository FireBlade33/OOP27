package lesson6dz;

public class Employee {
	public String name;
	public String surname;
	int salary;
	int cardAccount;
	String gender;
	int department;
	
	public Employee(String name, String surname, int salary, int cardAccount, String gender, int department){
		this.name = name;
		this.surname = surname;
		this.salary = salary;
		this.cardAccount = cardAccount;
		this.gender = gender;
		this.department = department;
	}
	
	
	
	

}
